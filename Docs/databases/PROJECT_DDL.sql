
CREATE TABLE AUTH_NAMES
(
  AUTH_NAME VARCHAR(200) NOT NULL COMMENT '접근권한명',
  UNIQUE_ID VARCHAR(200) NOT NULL COMMENT '접근권한 대표값',
  PRIMARY KEY (UNIQUE_ID)
) COMMENT '접근권한명';

CREATE TABLE AUTHS
(
  UNIQUE_ID_AUTH_NAMES VARCHAR(200) NOT NULL COMMENT '접근권한 대표값',
  UNIQUE_ID            VARCHAR(50)  NOT NULL COMMENT '유니크ID',
  PRIMARY KEY (UNIQUE_ID_AUTH_NAMES)
) COMMENT '권한들';

CREATE TABLE COMMONS_CODE
(
  COMMON_CODE_ID        varchar(40)   NOT NULL COMMENT '공통 코드_ID',
  NAME                  varchar(1000) NOT NULL COMMENT '명칭',
  ORDER_NUMBER          decimal       NULL     COMMENT '정렬순서',
  ATTRIBUTION_1         varchar(60)   NULL     COMMENT '속성_1',
  ATTRIBUTION_2         varchar(60)   NULL     COMMENT '속성_2',
  DESCRIPTION           varchar(1000) NULL     COMMENT '코드설명',
  SYSTEM_CODE_YN        varchar(40)   NOT NULL COMMENT '초기 시스템 코드 여부',
  USE_YN                varchar(40)   NOT NULL COMMENT '사용 여부',
  REGISTER_SEQ          varchar(50)   NOT NULL COMMENT '등록자SEQ',
  REGISTRY_DATE         varchar(20)   NOT NULL COMMENT '등록일자',
  MODIFIER_SEQ          varchar(40)   NOT NULL COMMENT '수정자_SEQ',
  MODIFY_DATE           varchar(20)   NOT NULL COMMENT '수정일자',
  SEX_ID                varchar(40)   NOT NULL COMMENT '성별_ID',
  PARENT_COMMON_CODE_ID varchar(40)   NULL     COMMENT '상위 공통 코드 ID',
  PRIMARY KEY (COMMON_CODE_ID)
);

CREATE TABLE COMPONENT
(
  COMPONENT    VARCHAR(500) NOT NULL COMMENT '성분',
  COMPONENT_ID VARCHAR(500) NOT NULL COMMENT '성분_ID',
  PRIMARY KEY (COMPONENT_ID)
) COMMENT '성분';

CREATE TABLE EFFECT
(
  EFFECT    VARCHAR(500) NOT NULL COMMENT '영양제 효과',
  EFFECT_ID VARCHAR(500) NOT NULL COMMENT '효과_ID',
  PRIMARY KEY (EFFECT_ID)
) COMMENT '영양제 효과';

CREATE TABLE HISTORY
(
  DISEASE    VARCHAR(50) NOT NULL COMMENT '질병이력',
  ALLERGIES  VARCHAR(50) NOT NULL COMMENT '알레르기',
  HISTORY_ID VARCHAR(50) NOT NULL COMMENT '과거력_ID',
  PRIMARY KEY (HISTORY_ID)
) COMMENT '과거력';

CREATE TABLE Inquiry
(
  INQUIRY_ID    VARCHAR(500) NOT NULL COMMENT '댓글_ID',
  WRITING_DATE  TIMESTAMP    NOT NULL COMMENT '작성일자',
  CONTENT       VARCHAR(500) NOT NULL COMMENT '내용',
  RESPONSE      VARCHAR(500) NOT NULL COMMENT '응답',
  RESPONSE_DATE TIMESTAMP    NOT NULL COMMENT '응답일자',
  ADMINE_ID     VARCHAR(500) NOT NULL COMMENT '관리자_ID',
  UNIQUE_ID     VARCHAR(50)  NOT NULL COMMENT '유니크ID',
  PRIMARY KEY (INQUIRY_ID)
) COMMENT '문의';

CREATE TABLE MEMBERSANDADMIN
(
  UNIQUE_ID        VARCHAR(50)  NOT NULL COMMENT '유니크ID',
  NAME             VARCHAR(50)  NOT NULL COMMENT '이름',
  ID               VARCHAR(50)  NOT NULL COMMENT '아이디',
  PASSWORD         VARCHAR(50)  NOT NULL COMMENT '비밀번호',
  BIRTHDATE        VARCHAR(50)  NOT NULL COMMENT '생년월일',
  AGE              VARCHAR(50)  NOT NULL COMMENT '나이',
  SEX_ID           VARCHAR(50)  NOT NULL COMMENT '성별_ID',
  ADDRESS          VARCHAR(50)  NOT NULL COMMENT '주소',
  PHONE            VARCHAR(50)  NOT NULL COMMENT '연락처',
  REGISTRATIONDATE VARCHAR(50)  NOT NULL COMMENT '가입일',
  EMAIL            VARCHAR(50)  NOT NULL COMMENT '이메일',
  ADMINE_ID        VARCHAR(500) NOT NULL COMMENT '관리자_ID',
  HISTORY_ID       VARCHAR(50)  NOT NULL COMMENT '과거력_ID',
  PRIMARY KEY (UNIQUE_ID)
) COMMENT '정보';

CREATE TABLE Review
(
  REVIEW_ID    VARCHAR(500) NOT NULL COMMENT '리뷰_ID',
  WRITING_DATE TIMESTAMP    NULL     COMMENT '작성일자',
  CONTENT      VARCHAR(500) NULL     COMMENT '내용',
  VIEW_COUNT   VARCHAR(500) NULL     COMMENT '조회수',
  SUPP_ID      VARCHAR(50)  NOT NULL COMMENT '영양제_ID',
  UNIQUE_ID    VARCHAR(50)  NOT NULL COMMENT '유니크ID',
  PRIMARY KEY (REVIEW_ID)
) COMMENT '리뷰';

CREATE TABLE SEX
(
  SEX    VARCHAR(50) NOT NULL COMMENT '성별',
  SEX_ID VARCHAR(50) NOT NULL COMMENT '성별_ID',
  PRIMARY KEY (SEX_ID)
) COMMENT '성별';

CREATE TABLE SIDEEFFECTS
(
  SIDEEFFECT    VARCHAR(500) NOT NULL COMMENT '부작용',
  SIDEEFFECT_ID VARCHAR(500) NOT NULL COMMENT '부작용_ID',
  PRIMARY KEY (SIDEEFFECT_ID)
) COMMENT '부작용';

CREATE TABLE SUPP_COMP
(
  SUPP_ID      VARCHAR(50)  NOT NULL COMMENT '영양제_ID',
  COMPONENT_ID VARCHAR(500) NOT NULL COMMENT '성분_ID'
) COMMENT '영양제별 성분리스트';

CREATE TABLE SUPP_LIST
(
  SUPP_ID     VARCHAR(50)  NOT NULL COMMENT '영양제_ID',
  EFFECT_ID   VARCHAR(500) NOT NULL COMMENT '효과_ID',
  EFFECT_FLAG VARCHAR(500) NOT NULL COMMENT '효과 FLAG'
) COMMENT '영양제 리스트';

CREATE TABLE SUPP_SIDE
(
  SUPP_ID       VARCHAR(50)  NOT NULL COMMENT '영양제_ID',
  SIDEEFFECT_ID VARCHAR(500) NOT NULL COMMENT '부작용_ID'
) COMMENT '영양제별 부작용';

CREATE TABLE SUPP_SPEC
(
  SUPP_ID     VARCHAR(50) NOT NULL COMMENT '영양제_ID',
  PRODUCT     VARCHAR(50) NOT NULL COMMENT '이름',
  MANUFACTURE VARCHAR(50) NOT NULL COMMENT '제조사',
  DOSAGE      VARCHAR(50) NOT NULL COMMENT '용량',
  FREQUENCY   VARCHAR(50) NOT NULL COMMENT '복용 주기',
  LOCATION    VARCHAR(50) NOT NULL COMMENT '이미지 위치',
  LINK        VARCHAR(50) NOT NULL COMMENT '링크',
  PRIMARY KEY (SUPP_ID)
) COMMENT '영양제 정보';

